const (
	// defaultNomadAddress is the local Nomad HTTP(S) endpoint that pmm-managed owns.
	defaultNomadAddress = "https://127.0.0.1:4646"
	// defaultNomadCACert is generated by the nomad service at startup.
	defaultNomadCACert = "/srv/nomad/certs/nomad-agent-ca.pem"
	// backupTaskName is the single task in the backup group.
	backupTaskName = "xtrabackup"
	// scriptPathEnv overrides the on-node payload command (handy for testing).
	scriptPathEnv = "PMM_BACKUP_SCRIPT_COMMAND"
	// defaultScriptPath is the MSP layout path where the payload is pre-installed.
	defaultScriptPath = "/usr/local/percona/msp/backup/bin/backup_xtrabackup.py"
)

// nomadClient is a thin HTTPS client for the subset of the Nomad API we use:
// register a batch job, list its allocations, and read allocation logs.
type nomadClient struct {
	address string
	http    *http.Client
}

// newNomadClient builds a client that trusts the Nomad CA when present and
// otherwise skips verification (localhost, self-signed, ACL disabled).
func newNomadClient() *nomadClient {
	address := defaultNomadAddress
	if a := os.Getenv("PMM_NOMAD_ADDRESS"); a != "" {
		address = a
	}

	tlsCfg := &tls.Config{InsecureSkipVerify: true} //nolint:gosec // localhost self-signed
	if ca, err := os.ReadFile(defaultNomadCACert); err == nil {
		pool := x509.NewCertPool()
		if pool.AppendCertsFromPEM(ca) {
			tlsCfg = &tls.Config{RootCAs: pool} //nolint:gosec
		}
	}

	return &nomadClient{
		address: address,
		http: &http.Client{
			Timeout:   30 * time.Second,
			Transport: &http.Transport{TLSClientConfig: tlsCfg},
		},
	}
}

// scriptCommand returns the on-node command that runs the payload.
func scriptCommand() string {
	if c := os.Getenv(scriptPathEnv); c != "" {
		return c
	}
	return defaultScriptPath
}

// --- Nomad job spec (minimal JSON mirror of the batch/raw_exec shape) ---

type nomadConstraint struct {
	LTarget string
	RTarget string
	Operand string
}

type nomadTemplate struct {
	EmbeddedTmpl string
	DestPath     string
	ChangeMode   string
	Perms        string
}

type nomadTask struct {
	Name      string
	Driver    string
	User      string
	Config    map[string]any
	Templates []*nomadTemplate
	Env       map[string]string
	Resources *nomadResources
}

type nomadResources struct {
	CPU      int
	MemoryMB int
}

type nomadRestartPolicy struct {
	Attempts int
	Interval int64
	Delay    int64
	Mode     string
}

type nomadReschedulePolicy struct {
	Attempts  int
	Unlimited bool
}

type nomadTaskGroup struct {
	Name             string
	Count            int
	RestartPolicy    *nomadRestartPolicy
	ReschedulePolicy *nomadReschedulePolicy
	Tasks            []*nomadTask
}

type nomadJob struct {
	ID          string
	Name        string
	Type        string
	Datacenters []string
	Meta        map[string]string
	Constraints []*nomadConstraint
	TaskGroups  []*nomadTaskGroup
}

// buildJobParams carries everything needed to render a dispatch job.
type buildJobParams struct {
	RunID         string
	ServiceID     string
	ConfigVersion int32
	NodeName      string
	RenderedYAML  string
}

// jobID is the unique Nomad job id for a run. It is derived from the run ID
// only (a UUID) so PMM service IDs — which contain slashes — never leak into
// the job name.
func jobID(runID string) string {
	return "pmm-backup-" + runID
}

// buildBackupJob renders the batch/raw_exec Nomad job that runs the payload on
// the target node. Config is delivered via an embedded template stanza (no
// artifact stanza — the script is pre-installed), and the node is selected with
// a ${node.unique.name} constraint. Restarts/reschedules are disabled so a
// failed backup is never silently retried.
func buildBackupJob(p buildJobParams) *nomadJob {
	id := jobID(p.RunID)
	return &nomadJob{
		ID:          id,
		Name:        id,
		Type:        "batch",
		Datacenters: []string{"*"},
		Meta: map[string]string{
			"backup_run_id":  p.RunID,
			"service_id":     p.ServiceID,
			"config_version": fmt.Sprintf("%d", p.ConfigVersion),
		},
		Constraints: []*nomadConstraint{{
			LTarget: "${node.unique.name}",
			RTarget: p.NodeName,
			Operand: "=",
		}},
		TaskGroups: []*nomadTaskGroup{{
			Name:             "backup",
			Count:            1,
			RestartPolicy:    &nomadRestartPolicy{Attempts: 0, Interval: int64(time.Minute), Delay: int64(time.Second), Mode: "fail"},
			ReschedulePolicy: &nomadReschedulePolicy{Attempts: 0, Unlimited: false},
			Tasks: []*nomadTask{{
				Name:   backupTaskName,
				Driver: "raw_exec",
				Config: map[string]any{
					"command": scriptCommand(),
					"args": []string{
						"-c", "${NOMAD_TASK_DIR}/backup_config.yaml",
						"--run-id", "${NOMAD_META_backup_run_id}",
						"--manifest", "${NOMAD_TASK_DIR}/manifest.json",
					},
				},
				Templates: []*nomadTemplate{{
					EmbeddedTmpl: p.RenderedYAML,
					DestPath:     "local/backup_config.yaml",
					ChangeMode:   "noop",
					Perms:        "0644",
				}},
				Env: map[string]string{
					"PERCONA_BACKUP_USER": "root",
					"HOME":                "/root",
				},
				Resources: &nomadResources{CPU: 500, MemoryMB: 512},
			}},
		}},
	}
}

// registerJob submits the job to Nomad and returns the evaluation ID.
func (c *nomadClient) registerJob(ctx context.Context, job *nomadJob) (string, error) {
	body, err := json.Marshal(map[string]any{"Job": job})
	if err != nil {
		return "", fmt.Errorf("failed to marshal nomad job: %w", err)
	}

	req, err := http.NewRequestWithContext(ctx, http.MethodPost, c.address+"/v1/jobs", bytes.NewReader(body))
	if err != nil {
		return "", err
	}
	req.Header.Set("Content-Type", "application/json")

	resp, err := c.http.Do(req)
	if err != nil {
		return "", fmt.Errorf("failed to register nomad job: %w", err)
	}
	defer resp.Body.Close() //nolint:errcheck

	data, _ := io.ReadAll(resp.Body)
	if resp.StatusCode != http.StatusOK {
		return "", fmt.Errorf("nomad register returned %d: %s", resp.StatusCode, string(data))
	}

	var out struct {
		EvalID string
	}
	if err := json.Unmarshal(data, &out); err != nil {
		return "", fmt.Errorf("failed to decode nomad register response: %w", err)
	}
	return out.EvalID, nil
}

// allocation is the subset of a Nomad allocation stub we need.
type allocation struct {
	ID           string
	ClientStatus string
}

// jobAllocations returns the allocations for a job, most-recent first is not
// guaranteed; callers pick the newest they care about.
func (c *nomadClient) jobAllocations(ctx context.Context, id string) ([]allocation, error) {
	req, err := http.NewRequestWithContext(ctx, http.MethodGet, c.address+"/v1/job/"+url.PathEscape(id)+"/allocations", nil)
	if err != nil {
		return nil, err
	}
	resp, err := c.http.Do(req)
	if err != nil {
		return nil, fmt.Errorf("failed to list allocations: %w", err)
	}
	defer resp.Body.Close() //nolint:errcheck

	data, _ := io.ReadAll(resp.Body)
	if resp.StatusCode != http.StatusOK {
		return nil, fmt.Errorf("nomad allocations returned %d: %s", resp.StatusCode, string(data))
	}
	var allocs []allocation
	if err := json.Unmarshal(data, &allocs); err != nil {
		return nil, fmt.Errorf("failed to decode allocations: %w", err)
	}
	return allocs, nil
}

// allocLogs reads the full stdout/stderr of a task in an allocation.
func (c *nomadClient) allocLogs(ctx context.Context, allocID, task, logType string) (string, error) {
	u := fmt.Sprintf("%s/v1/client/fs/logs/%s?task=%s&type=%s&plain=true&origin=start",
		c.address, url.PathEscape(allocID), url.QueryEscape(task), url.QueryEscape(logType))
	req, err := http.NewRequestWithContext(ctx, http.MethodGet, u, nil)
	if err != nil {
		return "", err
	}
	resp, err := c.http.Do(req)
	if err != nil {
		return "", fmt.Errorf("failed to read alloc logs: %w", err)
	}
	defer resp.Body.Close() //nolint:errcheck

	data, _ := io.ReadAll(resp.Body)
	if resp.StatusCode != http.StatusOK {
		return "", fmt.Errorf("nomad logs returned %d: %s", resp.StatusCode, string(data))
	}
	return string(data), nil
}
