package models_test

import (
	"fmt"
	"testing"
	"time"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
	"gopkg.in/reform.v1"
	"gopkg.in/reform.v1/dialects/postgresql"

	"github.com/percona/pmm/managed/models"
	"github.com/percona/pmm/managed/pi/common"
	"github.com/percona/pmm/managed/utils/testdb"
)

func TestCheckResults(t *testing.T) {
	sqlDB := testdb.Open(t, models.SkipFixtures, nil)
	t.Cleanup(func() {
		require.NoError(t, sqlDB.Close())
	})

	db := reform.NewDB(sqlDB, postgresql.Dialect, reform.NewPrintfLogger(t.Logf))
	q := db.Querier

	create := func(t *testing.T, cr *models.CheckResult) *models.CheckResult {
		t.Helper()
		require.NoError(t, models.CreateCheckResult(q, cr))
		return cr
	}

	t.Run("create and find", func(t *testing.T) {
		svc := "svc-find"
		labels := map[string]string{"k": "v"}
		cr := &models.CheckResult{
			CheckName:   "c1",
			AdvisorName: "advisor",
			Category:    "performance",
			Interval:    models.Standard,
			ServiceID:   svc,
			ServiceName: "find-me",
			ServiceType: models.MySQLServiceType,
			NodeID:      "node-id",
			NodeName:    "node-find",
			Status:      models.CheckResultFailed,
			Summary:     "summary",
			Description: "description",
			Severity:    int(common.Error),
			CheckedAt:   models.Now(),
		}
		require.NoError(t, cr.SetLabels(labels))
		create(t, cr)
		require.NotEmpty(t, cr.ID) // generated by CreateCheckResult

		got, err := models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc}, 0, 0)
		require.NoError(t, err)
		require.Len(t, got, 1)
		assert.Equal(t, cr.ID, got[0].ID)
		assert.Equal(t, models.CheckResultFailed, got[0].Status)
		assert.Equal(t, int(common.Error), got[0].Severity)
		assert.Equal(t, "node-find", got[0].NodeName)

		gotLabels, err := got[0].GetLabels()
		require.NoError(t, err)
		assert.Equal(t, labels, gotLabels)
	})

	t.Run("filters", func(t *testing.T) {
		svc := "svc-filter"
		create(t, &models.CheckResult{
			CheckName: "weak_pwd", Category: "security", ServiceID: svc, ServiceName: "ProdMySQL",
			NodeName: "node-A", Status: models.CheckResultFailed, Summary: "s",
			Severity: int(common.Critical), CheckedAt: models.Now(),
		})
		create(t, &models.CheckResult{
			CheckName: "old_ver", Category: "configuration", ServiceID: svc, ServiceName: "devmysql",
			NodeName: "node-B", Status: models.CheckResultOK, Summary: "s",
			Severity: int(common.Unknown), CheckedAt: models.Now(),
		})

		// service_name: case-insensitive substring.
		got, err := models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc, ServiceName: "prod"}, 0, 0)
		require.NoError(t, err)
		require.Len(t, got, 1)
		assert.Equal(t, "ProdMySQL", got[0].ServiceName)

		// node_name: case-insensitive substring.
		got, err = models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc, NodeName: "node-b"}, 0, 0)
		require.NoError(t, err)
		require.Len(t, got, 1)
		assert.Equal(t, "node-B", got[0].NodeName)

		// category: exact.
		got, err = models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc, Category: "security"}, 0, 0)
		require.NoError(t, err)
		require.Len(t, got, 1)

		// check_name: exact.
		got, err = models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc, CheckName: "old_ver"}, 0, 0)
		require.NoError(t, err)
		require.Len(t, got, 1)

		// severity: exact.
		sev := int(common.Critical)
		got, err = models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc, Severity: &sev}, 0, 0)
		require.NoError(t, err)
		require.Len(t, got, 1)
		assert.Equal(t, int(common.Critical), got[0].Severity)

		// status.
		st := models.CheckResultOK
		got, err = models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc, Status: &st}, 0, 0)
		require.NoError(t, err)
		require.Len(t, got, 1)
		assert.Equal(t, models.CheckResultOK, got[0].Status)

		count, err := models.CountCheckResults(q, models.CheckResultFilters{ServiceID: svc})
		require.NoError(t, err)
		assert.Equal(t, 2, count)
	})

	t.Run("time range filter", func(t *testing.T) {
		svc := "svc-time"
		create(t, &models.CheckResult{
			CheckName: "c", ServiceID: svc, ServiceName: "s", NodeName: "n",
			Status: models.CheckResultFailed, Summary: "s", CheckedAt: models.Now().Add(-48 * time.Hour),
		})
		create(t, &models.CheckResult{
			CheckName: "c", ServiceID: svc, ServiceName: "s", NodeName: "n",
			Status: models.CheckResultFailed, Summary: "s", CheckedAt: models.Now(),
		})

		from := models.Now().Add(-24 * time.Hour)
		got, err := models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc, From: &from}, 0, 0)
		require.NoError(t, err)
		require.Len(t, got, 1)
	})

	t.Run("pagination ordered by checked_at desc", func(t *testing.T) {
		svc := "svc-page"
		base := models.Now()
		for i := range 5 {
			create(t, &models.CheckResult{
				CheckName: fmt.Sprintf("c%d", i), ServiceID: svc, ServiceName: "s", NodeName: "n",
				Status: models.CheckResultFailed, Summary: "s", CheckedAt: base.Add(time.Duration(i) * time.Minute),
			})
		}

		// First page, newest first.
		got, err := models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc}, 0, 2)
		require.NoError(t, err)
		require.Len(t, got, 2)
		assert.Equal(t, "c4", got[0].CheckName)
		assert.Equal(t, "c3", got[1].CheckName)

		// Last page holds the single oldest row.
		got, err = models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc}, 2, 2)
		require.NoError(t, err)
		require.Len(t, got, 1)
		assert.Equal(t, "c0", got[0].CheckName)

		count, err := models.CountCheckResults(q, models.CheckResultFilters{ServiceID: svc})
		require.NoError(t, err)
		assert.Equal(t, 5, count)
	})

	t.Run("mark read", func(t *testing.T) {
		svc := "svc-mark"
		cr1 := create(t, &models.CheckResult{
			CheckName: "c1", ServiceID: svc, ServiceName: "s", NodeName: "n",
			Status: models.CheckResultFailed, Summary: "s", CheckedAt: models.Now(),
		})
		cr2 := create(t, &models.CheckResult{
			CheckName: "c2", ServiceID: svc, ServiceName: "s", NodeName: "n",
			Status: models.CheckResultFailed, Summary: "s", CheckedAt: models.Now(),
		})

		require.NoError(t, models.MarkCheckResultsRead(q, []string{cr1.ID}, true))

		isRead := true
		got, err := models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc, IsRead: &isRead}, 0, 0)
		require.NoError(t, err)
		require.Len(t, got, 1)
		assert.Equal(t, cr1.ID, got[0].ID)

		notRead := false
		got, err = models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc, IsRead: &notRead}, 0, 0)
		require.NoError(t, err)
		require.Len(t, got, 1)
		assert.Equal(t, cr2.ID, got[0].ID)

		// Unmark.
		require.NoError(t, models.MarkCheckResultsRead(q, []string{cr1.ID}, false))
		got, err = models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc, IsRead: &notRead}, 0, 0)
		require.NoError(t, err)
		require.Len(t, got, 2)
	})

	t.Run("cleanup old results", func(t *testing.T) {
		svc := "svc-clean"
		create(t, &models.CheckResult{
			CheckName: "old", ServiceID: svc, ServiceName: "s", NodeName: "n",
			Status: models.CheckResultFailed, Summary: "s", CheckedAt: models.Now().Add(-72 * time.Hour),
		})
		create(t, &models.CheckResult{
			CheckName: "new", ServiceID: svc, ServiceName: "s", NodeName: "n",
			Status: models.CheckResultFailed, Summary: "s", CheckedAt: models.Now(),
		})

		require.NoError(t, models.CleanupOldCheckResults(q, models.Now().Add(-24*time.Hour)))

		got, err := models.FindCheckResults(q, models.CheckResultFilters{ServiceID: svc}, 0, 0)
		require.NoError(t, err)
		require.Len(t, got, 1)
		assert.Equal(t, "new", got[0].CheckName)
	})
}
